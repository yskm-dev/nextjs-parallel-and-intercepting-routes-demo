export function getPhotoById(id: string): Photo | undefined {
  return photos.find((photo) => photo.id === id);
}

export function getPhotoIndex(id: string): number {
  return photos.findIndex((photo) => photo.id === id);
}

export function getAdjacentPhotoIds(id: string): { prevId?: string; nextId?: string } {
  const index = getPhotoIndex(id);

  if (index === -1) {
    return {};
  }

  return {
    prevId: photos[index - 1]?.id,
    nextId: photos[index + 1]?.id,
  };
}
